public class Kata {

    public static int makeNegative(final int x) {
        int respuesta = 0;
        if (x < 0) {
            respuesta = x;
        } else if (x > 0) {
            respuesta = -x;
        } else {
            respuesta = 0;
        }
        return respuesta; // Your code here.

    }

}
