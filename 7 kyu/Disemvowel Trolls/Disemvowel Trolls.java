public class Troll {
    public static String disemvowel(String str) {
        String result = str.replace("a", "").replace("e", "").replace("i", "").replace("o", "").replace("u", "").replace("A", "").replace("E", "").replace("I", "").replace("O", "").replace("U", "");
        return result;
    }
}
